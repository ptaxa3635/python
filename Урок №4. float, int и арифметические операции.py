# # и количества тыся. Например, есть число 46275 Необходимо возвести 7 (десятки) в степень 5 (единицы), умножить получившееся
# # число на 2 (сотни), и разделить на разность между 4 (десятки тысяч) и 6 (тысячи) то есть (4-6)В результате необходимо
# # получить вещественное число.
