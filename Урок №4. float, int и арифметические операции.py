# Урок N4. float, int и арифметические операции
# Задание N1 Пользователь вводит стороны прямоугольника, выведите его площадь и периметр. На вход программе могут
# подаваться как целые числа, так и вещественные

a = int(input("Первая сторона прямоугольника"))
b = int(input("Вторая сторона прямоугольника"))
s = a * b
p = 2 * a + 2 * b
print("Площадь прямоугольника", s, "Периметр прямоугольника", p)


